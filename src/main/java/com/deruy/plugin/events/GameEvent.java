package com.deruy.plugin.events;

/**
 * 서버 시즌 스케줄표에 있는 모든 이벤트(네더오픈, KOTH, 서플라이드랍, 바운티,
 * 빙고, 건축대회 등)가 공통으로 구현하는 인터페이스.
 *
 * 자동으로 날짜/시각에 맞춰 트리거하지 않고, 관리자가 인게임/콘솔 명령어로
 * 직접 시작·종료시키는 구조로 설계함 (일정표는 참고용, 실제 시작은 수동 트리거).
 */
public interface GameEvent {

    /** 이벤트를 식별하는 고유 이름. 명령어에서 이 이름으로 지칭한다. (예: "koth", "bingo", "bounty") */
    String getName();

    /** 이벤트 시작. 이미 실행 중이면 아무 동작 하지 않아야 한다. */
    void start();

    /** 이벤트 종료. 실행 중이 아니면 아무 동작 하지 않아야 한다. */
    void stop();

    /** 현재 실행 중인지 여부. */
    boolean isRunning();
}
